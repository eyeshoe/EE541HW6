# softmax function
def softmax(z):
    z = z - np.max(z, axis=1, keepdims=True)
    exp_z = np.exp(z)
    return exp_z / np.sum(exp_z, axis=1, keepdims=True)

# sigmoid functiom
def sigmoid(z):
    return np.where(z >= 0,
                  1 / (1 + np.exp(-z)),
                  np.exp(z) / (1 + np.exp(z)))

