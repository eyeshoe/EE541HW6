def gradient_descent(X_train, y_train, X_test, y_test,
                     learning_rate=0.1, max_iter=1000,
                     lambda_reg=0, reg_type=None, tol=1e-6):

    n_features = X_train.shape[1]
    n_classes = y_train.shape[1] #

    # initialize weights and bias
    W = np.zeros((n_features, n_classes))  # (784, 10)
    b = np.zeros(n_classes)                # (10,)

    train_losses, test_losses = [], []
    train_accs, test_accs = [], []

    for i in range(max_iter):
        # forward pass
        y_pred_train = softmax(X_train @ W + b)
        y_pred_test  = softmax(X_test @ W + b)

        # track loss and accuracy
        train_loss = cross_entropy_loss(y_train, y_pred_train)
        test_loss  = cross_entropy_loss(y_test, y_pred_test)
        train_losses.append(train_loss)
        test_losses.append(test_loss)

        # accuracy: pick highest probability class and compare to true label
        train_accs.append(np.mean(np.argmax(y_pred_train, axis=1) == np.argmax(y_train, axis=1)))
        test_accs.append(np.mean(np.argmax(y_pred_test, axis=1)  == np.argmax(y_test, axis=1)))

        # convergence check
        if i > 0 and abs(train_losses[-2] - train_losses[-1]) < tol:
            print(f"Converged at iteration {i}")
            break

        # compute gradients and update
        grad_W, grad_b = compute_gradient(X_train, y_train, y_pred_train, W, lambda_reg, reg_type)
        W -= learning_rate * grad_W
        b -= learning_rate * grad_b

    return W, b, train_losses, test_losses, train_accs, test_accs

