def sgd(X_train, y_train, X_test, y_test,
        learning_rate=0.1, max_epochs=10,
        batch_size=1, record_every=5000):

    n_samples, n_features = X_train.shape
    n_classes = y_train.shape[1]

    W = np.zeros((n_features, n_classes))
    b = np.zeros(n_classes)

    train_losses, test_losses = [], []
    train_accs, test_accs = [], []
    samples_seen = 0
    next_record = 0  # record at sample 0, 5000, 10000, etc.

    def record_metrics():
        y_pred_train = softmax(X_train @ W + b)
        y_pred_test  = softmax(X_test @ W + b)
        train_losses.append(cross_entropy_loss(y_train, y_pred_train))
        test_losses.append(cross_entropy_loss(y_test, y_pred_test))
        train_accs.append(np.mean(np.argmax(y_pred_train, axis=1) == np.argmax(y_train, axis=1)))
        test_accs.append(np.mean(np.argmax(y_pred_test,  axis=1) == np.argmax(y_test,  axis=1)))

    # record initial performance before any training
    record_metrics()
    next_record = record_every

    for epoch in range(max_epochs):
        # shuffle data at the start of each epoch
        idx = np.random.permutation(n_samples)
        X_shuffled = X_train[idx]
        y_shuffled = y_train[idx]

        for start in range(0, n_samples, batch_size):
            # grab mini batch
            X_batch = X_shuffled[start:start + batch_size]
            y_batch = y_shuffled[start:start + batch_size]

            # forward pass and update
            y_pred = softmax(X_batch @ W + b)
            grad_W, grad_b = compute_gradient(X_batch, y_batch, y_pred, W)
            W -= learning_rate * grad_W
            b -= learning_rate * grad_b

            samples_seen += batch_size

            # record every 5000 samples
            if samples_seen >= next_record:
                record_metrics()
                next_record += record_every

    return W, b, train_losses, test_losses, train_accs, test_accs

