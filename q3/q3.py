# compute gradient for n namples
def compute_gradient(X, y_true, y_pred, W, lambda_reg=0, reg_type=None):
    n_samples = X.shape[0]

    # difference between predicted probabilities and true one-hot labels
    diff = y_pred - y_true  # shape: (n_samples, 10)

    # gradient for weights and bias
    grad_W = X.T @ diff / n_samples  # shape: (784, 10)
    grad_b = np.mean(diff, axis=0)   # shape: (10,)

    # regularization
    if reg_type == 'l2' and lambda_reg > 0:
        grad_W += lambda_reg * W
    elif reg_type == 'l1' and lambda_reg > 0:
        grad_W += lambda_reg * np.sign(W)

    return grad_W, grad_b

